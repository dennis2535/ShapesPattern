
public class SquarePattern {
    public static void main(String[] args) {
        int shape = 5;
        
        for (int i = 1; i < 6; i++){
            for (int j = 1; j < 6; j++) {
                System.out.print(" * ");
                shape++;
                
            }
            
            System.out.println();
        }
    }
    
}
